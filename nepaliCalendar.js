// Reference: BS 2000-01-01 = AD 1943-04-14 (Wednesday)
const REF_BS_YEAR = 2000;
const REF_AD_DATE = new Date(Date.UTC(1943, 3, 14)); // month is 0-indexed

export const bsMonthsEn = ["Baisakh","Jestha","Ashadh","Shrawan","Bhadra","Ashwin","Kartik","Mangsir","Poush","Magh","Falgun","Chaitra"];
export const bsMonthsNe = ["बैशाख","जेष्ठ","असार","श्रावण","भदौ","असोज","कार्तिक","मंसिर","पुस","माघ","फाल्गुन","चैत"];

const digitsNe = ["०","१","२","३","४","५","६","७","८","९"];
export const toNepaliDigits = (n) => String(n).split('').map(d => /\d/.test(d) ? digitsNe[Number(d)] : d).join('');

export function adToBs(adDate) {
  const utc = Date.UTC(adDate.getFullYear(), adDate.getMonth(), adDate.getDate());
  let diffDays = Math.floor((utc - REF_AD_DATE.getTime()) / 86400000);
  if (diffDays < 0) return null;

  let year = REF_BS_YEAR;
  let month = 0;
  let day = 1;

  while (true) {
    const months = bsMonthData[year];
    if (!months) return null;
    const daysInMonth = months[month];
    if (diffDays < daysInMonth) {
      day = 1 + diffDays;
      break;
    }
    diffDays -= daysInMonth;
    month++;
    if (month >= 12) {
      month = 0;
      year++;
    }
  }
  return { year, month, day };
}

export function getBsMonthLength(year, month) {
  return bsMonthData[year]?.[month] || 30;
}

// Get AD date for BS year/month/day=1 (for computing starting weekday)
export function bsToAd(year, month, day) {
  let totalDays = 0;
  for (let y = REF_BS_YEAR; y < year; y++) {
    const m = bsMonthData[y];
    if (!m) return null;
    totalDays += m.reduce((a, b) => a + b, 0);
  }
  const months = bsMonthData[year];
  if (!months) return null;
  for (let mo = 0; mo < month; mo++) totalDays += months[mo];
  totalDays += (day - 1);
  const result = new Date(REF_AD_DATE.getTime() + totalDays * 86400000);
  return result;
}
